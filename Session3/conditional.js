let score = 75;

if (score >= 90) {
    console.log("A Grade");
} else if (score >= 60) {
    console.log("B Grade");
} else {
    console.log("C Grade");
}

// switch example
let fruit = "apple";
switch (fruit) {
    case "apple": console.log("Red fruit"); break;
    case "banana": console.log("Yellow fruit"); break;
    default: console.log("Unknown fruit");
}
